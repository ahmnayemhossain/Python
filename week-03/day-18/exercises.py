print("Day 18 practice exercises")

print("\nExercise 1: Upper and lower")
text = input("Enter text: ")
print(text.upper())
print(text.lower())

print("\nExercise 2: Slice first three characters")
word = input("Enter a word: ")
print(word[:3])

print("\nExercise 3: Reverse string")
print(word[::-1])

print("\nExercise 4: Remove spaces")
sentence = input("Enter a sentence with spaces: ")
print(sentence.strip())

print("\nExercise 5: Count character")
character = input("Enter one character to count: ")
print(sentence.count(character))
